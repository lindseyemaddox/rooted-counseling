		<p>Courtney is a born and raised Nashvillian. After obtaining an undergraduate degree in Psychology from Lipscomb University, Courtney and her husband moved to Knoxville where she earned a Master’s Degree in Clinical Mental Health Counseling at the University of Tennessee. During her time in Knoxville, Courtney worked with at-risk children and adolescents. When she and her husband were presented with the opportunity to move back to their hometown of Nashville, she was thrilled to bring her experience and establish a new practice in her home community.</p>

		<p>Courtney believes that counseling is a collaborative process and enjoys working alongside her clients during their time of need. She believes that every individual is the expert of his or her own life and looks forward to learning from each client. While Courtney understands the value of counseling, she is also sensitive to the fact that counseling can be uncomfortable for clients at times. Therefore, as one of her main goals is to decrease discomfort and make the counseling relationship one that feels warm and secure, Courtney is intentional about letting counseling be a time for her clients to explore their needs and desires as they see fit.</p>

		<p>Courtney practices from a Cognitive Behavioral Therapy framework to provide her clients with clarity on the relationship between their emotions, behaviors, and thoughts. Courtney believes that when clients gain insight about these three areas, they are empowered with a deeper understanding of who they are and a freedom to carry that knowledge forward for the rest of their lives. In addition to focusing on the individual, Courtney believes there is value in considering how that individual is shaped by family, friends, and other groups. She also works from a systems perspective as we are often shaped by our families, friends, and other groups with which we live.</p>

		<p>In a counseling relationship, clients are vulnerable and often share things that seem scary and embarrassing. Courtney does not take her role in this relationship lightly. Courtney is passionate about providing a safe space in moments that are painful for her clients. As Courtney understands that life is messy, she is passionate about providing a safe space for her clients to process through the gray areas of difficult and frustrating times.</p>

		<p>In her spare time, Courtney enjoys reading, traveling with her husband and girlfriends, cooking, spending time with her family, and trying all of the new restaurants the greater Nashville area has to offer!</p>

